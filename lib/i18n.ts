export function useTranslation(locale: string = 'es') {
  const t = (key: string): string => {
    return translations[locale]?.[key] || translations['es']?.[key] || key;
  };

  return { t, locale };
}

export function getTranslation(key: string, locale: string = 'es'): string {
  return translations[locale]?.[key] || translations['es']?.[key] || key;
}
